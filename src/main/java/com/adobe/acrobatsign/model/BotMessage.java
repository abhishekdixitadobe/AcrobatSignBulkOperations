package com.adobe.acrobatsign.model;

public class BotMessage {

	private String message;

	public BotMessage() {
	}

	public BotMessage(String message) {
		this.message = message;
	}

	public String getMessage() {
		return this.message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

}
